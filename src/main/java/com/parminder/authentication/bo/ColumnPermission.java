package com.parminder.authentication.bo;

	
public class ColumnPermission {

	

	boolean read;

	boolean write;

	public boolean isRead() {
		return read;
	}

	public void setRead(boolean read) {
		this.read = read;
	}

	public boolean isWrite() {
		return write;
	}

	public void setWrite(boolean write) {
		this.write = write;
	}


}
